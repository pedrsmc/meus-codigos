public class ContagemRegressiva {
    public static void main(String[] args) {
        int contador = 10;
        
        System.out.println("Contagem regressiva iniciada:");
        
        while (contador >= 1) {
            System.out.println(contador);
            contador--;
        }
        
        System.out.println("Contagem regressiva conclu√≠da.");
    }
}